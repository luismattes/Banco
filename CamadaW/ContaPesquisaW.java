    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        while (model.getRowCount() > 0) {
            int i = model.getRowCount() - 1;
            model.removeRow(i);
        }
        Conta conta;
        ArrayList<Conta> lista = controlador.SearchConta();
        for (int i = 0; i < lista.size(); i++) {
            conta = lista.get(i);
            model.insertRow(model.getRowCount(), new Object[] {conta.Get_id_conta(),
            conta.Get_data_cadastro(), conta.Get_saldo(), conta.Get_limite(), 
            conta.Get_tipo_conta()});
        }
    }//GEN-LAST:event_jButton1ActionPerformed

