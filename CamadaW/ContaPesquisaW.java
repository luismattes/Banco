    private void jButtonPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPDFActionPerformed
//        String path = "";
//        JFileChooser j = new JFileChooser();
//        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
//        int i = j.showSaveDialog(this);
//        
//        if (i == JFileChooser.APPROVE_OPTION) {
//            path = j.getSelectedFile().getPath();
//        }
        
        Document doc = new Document();
        try {
            PdfWriter.getInstance(doc, new FileOutputStream("C:\\Users\\Luis\\RelatorioConta.pdf"));
            doc.open();
            doc.setPageSize(PageSize.A4);
            PdfPTable table = new PdfPTable(5);
            
            table.addCell("Numero");
            table.addCell("Cadastro");
            table.addCell("Saldo");
            table.addCell("Limite");
            table.addCell("Tipo");
            
            for (int k = 0; k < model.getRowCount(); k++) {
                String id = model.getValueAt(k, 0).toString();
                String nome = model.getValueAt(k, 1).toString();
                String contato = model.getValueAt(k, 2).toString();
                String tp = model.getValueAt(k, 3).toString();
                String tipo = model.getValueAt(k, 4).toString();
                
                table.addCell(id);
                table.addCell(nome);
                table.addCell(contato);
                table.addCell(tp);
                table.addCell(tipo);
            }
            
            doc.add(table);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(PessoaPesquisarW.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(PessoaPesquisarW.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            doc.close();
        }
    }//GEN-LAST:event_jButtonPDFActionPerformed

